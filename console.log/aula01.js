// ================ CONSOLE.LOG =================
//  O objetivo da aula é  mostrar ao comando console.log
//  que tem como função escrever algo na tela
//  esta função utiliza parametros (instruções que
//  ficam dentro os parenteses)
//  
//  Os parametros podem ser textos, números decimais, 
//  numeros flutuantes, etc, etc..
// ==============================================

console.log("Olá, amigo!");
console.log("'Olá, amigo'");
console.log('"Olá, amigo"');
console.log(`"'Olá, amigo'"`);
console.log(2500);
console.log(3.14);

/** Também é possível concatenar (juntar) duas instruções diferentes
 *  com os operadores '+' e ','
 */
console.log(`\nOlá,` + ` ` + `amigo`);
console.log(2 + 'Olá amigo');
console.log(`Olá,`, `amigo`);
console.log(123456, 15.5, 'Olá amigo\n');

//Se utilizar o operador + com dois números, o resultado exibido será uma soma
console.log(2 + 2);
